public class Program {

    //---------- Print Methods ----------
    public static void print(int i) {
        System.out.print(i);
    }

    public static void print(char c) {
        System.out.print(c);
    }

    public static void print(String s) {
        System.out.print(s);
    }

    public static void print(char[] s) {
        System.out.print(s);
    }

    public static void print(Object o) {
        System.out.print(o);
    }
}
